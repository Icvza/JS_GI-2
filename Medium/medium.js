let input = parseInt(prompt('Give me number'))

function month(input) {
    if (input === 1) {
        alert('January')
    } else if (input === 2) {
        alert('February')
    } else if (input === 3) {
        alert('March')
    } else if (input === 4) {
        alert('April')
    } else if (input === 5) {
        alert('May')
    } else if (input === 6) {
        alert('June')
    } else if (input === 7) {
        alert('July')
    } else if (input === 8) {
        alert('August')
    } else if (input === 9) {
        alert('September')
    } else if (input === 10) {
        alert('October')
    } else if (input === 11) {
        alert('November')
    } else if (input === 12) {
        alert('December')
    } else {
        alert('Invalid month')
    }

}

month(input)
